package t2.classes;

public class Calculadora {
    
    // soma
    public static double soma(double n1, double n2) {
        return n1 + n2;
    }
    public static double soma(double n1, double n2, double n3) {
        return n1 + n2 + n3;
    }

    public static double soma(double n1, double n2, double n3, double n4) {
        return n1 + n2 + n3 + n4;
    }

    public static double subtracao(double n1, double n2) {
        return n1 - n2;
    }

    public static double subtracao(double n1, double n2, double n3) {
        return n1 - n2 - n3;
    }

    public static double subtracao(double n1, double n2, double n3, double n4) {
        return n1 - n2 - n3 - n4;
    }

    // multiplicação
    public static double multiplicar(double n1, double n2) {
        return n1 * n2;
    }

    public static double multiplicar(double n1, double n2, double n3) {
        return n1 * n2 * n3;
    }

    public static double multiplicar(double n1, double n2, double n3, double n4) {
        return n1 * n2 * n3 * n4;
    }

    public static double dividir(double n1, double n2) {
        return n1 / n2;
    }
    public static double dividir(double n1, double n2, double n3) {
        return n1/n2/n3;
    }
    public static double dividir(double n1, double n2, double n3, double n4) {
        return n1/n2/n3/n4;
    }
}